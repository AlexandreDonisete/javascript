/*                                         REGRAS PARA LET
-Não podemos criar váriaves com pálavras reservadas.
-Variáveis precisam ter nomes significativos.
-Não podemos começar nomes de uma variável com números.
-Não pode conter espaços ou traços.
-Para criar variáveis com nomes compostos, utilizar camelCase (Exemplo let nomeCompletoDoCliente = 'Alexandre Donisete").
-Case-sensitive, ou seja, as variáveis mudam caso as letras estejas maiúsculas (Exemplo let nomecliente e nomeCliete são variáveis diferentes. ) 
-Não podemos redeclaras variáveis com let.
-NÃO UTILIZE VAR, UTILIZE LET.
*/
 
