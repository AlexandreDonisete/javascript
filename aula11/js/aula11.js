/** Funções dentro de objetos são chamadas de métodos, caso esteja fora do objeto chamamos de função. */

alert ('Com a nossa mensagem.'); /**Faz um Alerta */
confirm ('Deseja realmente apagar?'); /**Função Boolean que entrega um resultado true ou false */
prompt ('Digite seu nome'); /**Função que exibe uma caixa de texto */ 
/**As funções acima são abreviações dos respectivos métodos window.alert, window.confirm e window.prompt. */
